let numero=4;

switch (numero) {
    case 1:
        console.log("El numero es 1");
    break;
    case 2:
        console.log("El numero es 2");
    break;
    case 3:
        console.log("El numero es 3");
    break;
    case 4:
        console.log("El numero es 4");
    break;
    case 5:
        console.log("El numero es 5");
    break;

    default:
        console.log("El numero es diferente a los 5 primeros");
    break;
}